<p>&nbsp;</p>

<p>虎爺轎班身穿著黃色虎紋衣、神轎造型與日本神轎相似、步伐也有日本味，昔虎爺轎過去會上下搖動，今規定不可，獨樹一幟的風格更全國虎爺會爭相模仿的榜樣，也是北港媽祖出巡中最會「喫炮」的神轎，在遶境行列中，虎虎生風的抬轎方式，在熊熊的炮火中威風凜凜的英姿，令人敬畏著迷，砲火濃煙漫天之中、轟隆作響的場景中，「虎爺轎」屹立不搖，轎夫無畏無懼的神采，讓群眾懾服，更有全台唯一的「喫炮虎」之名聲，除了農曆三月十九日「北港迎媽祖」外，每年農曆正月十五上元遶境，虎爺也會出轎參與遶境，後改為虎年元宵節才會出轎。</p>

<p><img alt="" src="http://pic.mazu.ioa.tw/upload/ckeditor_pictures/name/0/0/0/29/400w_781957577_56fe349bf09be.jpg" style="width: 400px; height: 267px;" /><br />
▲ 神轎造型與日本神轎相似的虎爺轎 (<a href="https://www.facebook.com/comdan66" target="_blank">吳政賢</a> 攝)</p>

<p>&nbsp;</p>

<p><strong>虎爺嘛驚炸彈</strong>，在1983年農曆三月十九日晚上，傳出朝天宮內之「八角琉璃長明燈」香火無故熄滅，緊接著就發生虎爺轎在北港鎮民主路「玉時軒」商店前遭遇土製炸彈之難，當時虎爺轎解體，三尊虎爺均有受損，抬轎者多人送醫急救，當時三虎神像還「飛」到屋頂上，於是當年虎爺會停止遶境活動。</p>

<p>隔年（1984年）就在農曆三月二十日入廟時分，六媽亦遭土製炸彈之殃，虎爺臨身起乩，模樣像老虎般，走起路來虎虎生風，停止時也會捋虎鬍鬚，檢查著每一堆虎爺轎要吃的鞭炮，有時興奮之際，也會要求喫炮，由祂獨自蹲在一大堆的鞭炮上將其燃放，看過的人莫不嘆為觀止，接連稱呼「勇喔！」，該事件也被戲稱「虎爺嘛驚炸彈」。</p>

<p><img alt="" src="http://pic.mazu.ioa.tw/upload/ckeditor_pictures/name/0/0/0/28/400w_156598598_56fe25c8cb559.jpg" style="width: 400px; height: 600px;" /><br />
▲ 虎爺喫炮的蕈狀雲 (<a href="https://www.facebook.com/comdan66" target="_blank">吳政賢</a> 攝)</p>

<p>&nbsp;</p>

<p>今北港鎮的虎爺在每年農曆三月十九、二十日跟隨北港朝天宮媽祖出巡，虎爺「喫炮」成為了觀光客或愛好刺激的民眾不可不看的重頭戲。各地的虎爺會或有供奉虎爺的廟宇，近年來也開始效法北港虎爺的喫炮方式，並自成一格，但北港虎爺的精神及特有步法始終無法承習。也因鞭炮燃放方式不同而衍生出了多種喫炮方式，欲欣賞虎爺喫炮的強大威力，可於每年農曆三月十九、二十日來北港參與「北港迎媽祖」之盛事。</p>

