    <p>來參與過北港十九廟會的都知道，每當十九上午廟內還在為上午出巡做準備時，廟外就傳來陣陣的鞭炮聲，從廟後、廟旁、廟門一路的鞭炮聲傳出，如果是在地北港人的話，不用多想就知道誰來了，這時候立馬跑去廟埕一看，沒錯！正是這群北港的黃袍勇士們 虎爺！</p>

<p>廟會都還沒開廟門，虎爺會的每個人身上的黃虎皮戰甲都已經上了一層炮灰，可見北港虎爺將在這場盛會中的角色定位，早上起馬炮完後，接著就可以聽到 咚、咚、咚 聲響起，虎爺轎子抬立起來，轎班人員將民眾稍微疏散開來，直扇人員引導著扛轎人員往前方的炮堆移動，沒錯，是 30箱炮，或是更多箱？咚、咚、咚 聲又響起，大家屏息以待，轟 的一聲，天空出現了蕈狀雲！</p>

<p><img alt="" src="http://pic.mazu.ioa.tw/upload/ckeditor_pictures/name/0/0/0/24/400w_925135687_56fe23087a3a4.jpg" style="line-height: 1.6; width: 400px; height: 601px;" /><br />
