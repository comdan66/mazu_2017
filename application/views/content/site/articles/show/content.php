<p>&nbsp;</p>

<p>北港朝天宮的虎爺會在每年農曆三月十九、二十日跟隨北港朝天宮媽祖出巡，而虎爺「喫炮」成為了觀光客或愛好刺激的民眾不可不看的重頭戲之一，在北港有句俗語：「<strong>暗街仔暗眠摸，溪仔底虎爺公</strong>」，即意指傳統虎爺信仰區域在溪仔底（今北港鎮博愛路，於民主路和中正路之間的路段），故溪仔底自古又被稱為「虎爺街」，每當繞境到此街上轎班人員必須以癲轎方式行走並口喊「<strong>風來虎嘯</strong>」，以感念先賢對虎爺會的無私奉獻，想要看最傳統的虎爺癲轎，就要把握農曆三月十九、二十虎爺行經博愛路段時的機會喔！</p>

<p><img alt="" src="http://pic.mazu.ioa.tw/upload/ckeditor_pictures/name/0/0/0/27/400w_1723740171_56fe259456fac.jpg" style="line-height: 1.6; width: 400px; height: 534px;" /><br />
